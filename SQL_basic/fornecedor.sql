CREATE OR REPLACE FUNCTION formata_texto (valor VARCHAR(100)) RETURNS VARCHAR(100)
BEGIN
	DECLARE primeira_letra VARCHAR(1);
    DECLARE resto VARCHAR(99);
    DECLARE resultado VARCHAR(100);
    
    SET primeira_letra = UPPER(substr(valor, 1, 1));
    SET resto = LOWER(SUBSTR(valor, 2,LENGTH(valor)));
    SET resultado = CONCAT(primeira_letra, resto);
    
    RETURN resultado;
END;

